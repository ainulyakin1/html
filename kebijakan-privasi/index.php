        <h2>2. Penggunaan Informasi Pribadi Yang Kami Kumpulkan</h2>
        <p>
            Kami dapat menggunakan Informasi Pribadi yang dikumpulkan untuk tujuan sebagai berikut maupun untuk tujuan lain yang diizinkan oleh peraturan perundang-undangan yang berlaku (“Tujuan”):
        </p>

        <p><strong>Jika anda adalah pengguna</strong> (pengguna akhir, misal: customer laundry), kami dapat menggunakan Informasi Pribadi anda:</p>
        <ol>
            <li>Mengidentifikasi dan mendaftarkan anda sebagai pengguna dan untuk mengadministrasikan, memverifikasi, menonaktifkan, atau mengelola akun anda.</li>
            <li>Memfasilitasi atau memungkinkan verifikasi apa pun yang menurut pertimbangan kami diperlukan, sebelum penyedia layanan memberikan layanan kepada anda atau sebelum kami mendaftarkan anda sebagai pengguna, termasuk proses Mengenal Pelanggan (Know Your Customer).</li>
            <li>Memungkinkan penyedia layanan untuk menyediakan layanan yang anda minta.</li>
            <li>Mengolah dan memfasilitasi pesanan dan transaksi pembayaran yang anda lakukan.</li>
            <li>Memberitahu anda mengenai segala pembaruan pada Aplikasi atau perubahan pada layanan yang disediakan.</li>
            <li>Mengolah dan menanggapi pertanyaan dan saran yang diterima dari anda.</li>
            <li>Memelihara, mengembangkan, menguji, meningkatkan, dan mempersonalisasikan Aplikasi untuk memenuhi kebutuhan, dan preferensi anda sebagai pengguna.</li>
            <li>Memantau dan menganalisis aktivitas, perilaku, dan data demografis pengguna termasuk kebiasaan dan penggunaan berbagai layanan yang tersedia di Aplikasi.</li>
            <li>Menawarkan atau menyediakan layanan dari afiliasi atau mitra kami.</li>
            <li>Mengirimi anda komunikasi pemasaran, iklan, voucher, survei, dan informasi secara langsung atau terfokus, serta informasi tentang penawaran atau promosi khusus.</li>
