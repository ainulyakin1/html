            <li>Cara untuk menghubungi kami</li>
        </ol>

        <h2>1. Informasi Pribadi Yang Kami Kumpulkan</h2>
        <p>
            Kami mengumpulkan informasi yang digunakan untuk mengidentifikasi, menghubungi, atau menemukan orang atau perangkat yang terkait dengan informasi tersebut (“Informasi Pribadi”). Informasi Pribadi termasuk, tetapi tidak terbatas pada, nama, alamat, tanggal lahir, nomor telepon, alamat e-mail, rekening bank, dan jenis kelamin.
        </p>
        <p>
            Selain itu, untuk informasi lainnya, seperti profil pribadi, dan atau nomor pengenal unik (nomor ID), yang dikaitkan atau digabungkan dengan Informasi Pribadi, maka informasi tersebut juga dianggap sebagai Informasi Pribadi.
        </p>
        <p>
            Informasi Pribadi yang kami kumpulkan dapat diberikan oleh anda secara langsung atau oleh pihak ketiga (misalnya: ketika anda mendaftar atau menggunakan Aplikasi, ketika anda menghubungi layanan pelanggan kami, atau sebaliknya ketika anda memberikan Informasi Pribadi kepada kami).
        </p>
        <p>
            Kami dapat mengumpulkan informasi dalam berbagai macam bentuk dan tujuan (termasuk tujuan yang diizinkan berdasarkan peraturan perundang-undangan yang berlaku).
        </p>

