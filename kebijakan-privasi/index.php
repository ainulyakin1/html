            <li>Mengidentifikasi dan mendaftarkan anda sebagai penyedia layanan dan untuk mengadministrasi, mengelola, atau memverifikasi Akun anda.</li>
            <li>Memfasilitasi atau memungkinkan verifikasi apapun yang menurut pertimbangan kami perlu dilakukan sebelum kami dapat mendaftarkan anda sebagai penyedia layanan.</li>
            <li>Memungkinkan anda memberikan layanan kepada pengguna.</li>
            <li>Mengolah, memfasilitasi, dan menyelesaikan pembayaran kepada anda terkait dengan layanan yang telah anda berikan.</li>
            <li>Memberikan anda pemberitahuan dan pembaruan pada Aplikasi atau perubahan cara penyediaan layanan.</li>
            <li>Memberi anda laporan terkait dengan layanan yang telah anda sediakan.</li>
            <li>Memproses dan menanggapi saran dari pengguna terhadap layanan yang telah anda berikan.</li>
            <li>Memelihara, mengembangkan, menguji, meningkatkan, dan mempersonalisasikan Aplikasi untuk memenuhi kebutuhan dan preferensi anda sebagai penyedia layanan.</li>
            <li>Memantau dan menganalisis aktivitas, perilaku, dan data demografis pengguna termasuk kebiasaan dan daya tanggap penyedia layanan untuk berbagai layanan yang tersedia pada Aplikasi.</li>
            <li>Menawarkan atau menyediakan layanan dari afiliasi atau mitra kami.</li>
