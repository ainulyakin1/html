        <h2>Umum</h2>
        <p>Kebijakan Privasi ini menjelaskan bagaimana Smartlink (PT Ada Ide Langsung Jalan atau dalam hal ini disebut sebagai “kami”) mengumpulkan, menyimpan, menggunakan, mengolah, menguasai, mentransfer, mengungkapkan, dan melindungi Informasi Pribadi pengguna (dalam hal ini disebut sebagai “anda”). Kebijakan Privasi ini berlaku bagi seluruh pengguna aplikasi, situs web (dashboard.smartlink.id dan Smartlink.id), layanan, atau produk (“Aplikasi”) kami, kecuali diatur pada kebijakan privasi yang terpisah.</p>

        <p>Mohon anda membaca keseluruhan Kebijakan Privasi ini dengan saksama untuk memastikan bahwa anda memahami bagaimana proses pengolahan data kami. Kecuali didefinisikan lain, semua istilah dengan huruf kapital yang digunakan dalam Kebijakan Privasi ini memiliki arti yang sama dengan yang tercantum dalam Ketentuan Penggunaan.</p>

        <p><strong>Kebijakan Privasi ini mencakup hal-hal sebagai berikut:</strong></p>
        <ol>
            <li>Informasi Pribadi yang kami kumpulkan</li>
            <li>Penggunaan Informasi Pribadi yang kami kumpulkan</li>
            <li>Pemberian Informasi Pribadi yang kami kumpulkan</li>
            <li>Penyimpanan Informasi Pribadi</li>
            <li>Akses dan koreksi Informasi Pribadi</li>
            <li>Tempat kami menyimpan Informasi Pribadi Anda</li>
